package com.dio;

public class ExercicioVTO2 {
    public static void main(String[] args){
        // Exercicio: Criar duas variáveis para cada tipo de dado
        int num1 = 100;
        int num2 = 200;
        byte b1 = 8;
        byte b2 = 16;
        short s1 = 32;
        short s2 = 64;
        long l1 = 10000000L;
        long l2 = 10000001L;
        float pontoFlutuante1 = (float)3.14;
        float pontoFlutuante2 = (float)1.6;
        double duplo1 = 110.11;
        double duplo2 = 112.33;
        boolean verdadeiro = true;
        boolean falso = false;
        String nome = "Júlio";
        String sobrenome = "Goes";
        char c1 = 'J';
        char c2 = 'C';
        
        System.out.println("Inteiro: " + num1 + ", " + num2);
        System.out.println("Byte: " + b1 + ", " + b2);
        System.out.println("Short: " + s1 + ", " + s2);
        System.out.println("Long: " + l1 + ", " + l2);
        System.out.println("Floats: " + pontoFlutuante1 + ", " + pontoFlutuante2);
        System.out.println("Double: " + duplo1 + ", " + duplo2);
        System.out.println("Boolean: " + verdadeiro + ", " + falso);
        System.out.println("String: " + nome + ", " + sobrenome);
        System.out.println("Char: " + c1 + ", " + c2);

    }
}
